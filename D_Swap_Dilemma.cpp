void solve() {
    int n;
    cin >> n;
    vi a(n), b(n);
    pbds os;
    map<int, int> mpp;
    for (auto& it : a) {
        cin >> it;
    }
    for (int i = 0; i < n; i++) {
        cin >> b[i];
        mpp[b[i]] = i;
    }
    if (n == 1) {
        if (a[0] == b[0])
            py;
        else
            pn;
        return;
    }
    for (int i = 0; i < n - 2; i++) {
        if (mpp.find(a[i]) == mpp.end()) {
            pn;
            return;
        }
        int pos = mpp[a[i]];
        int extra = (int)os.size() - (int)os.order_of_key(pos);
        pos += extra;
        int par = pos - i;
        if (par % 2) {
            swap(a[n - 1], a[n - 2]);
        }
        os.insert(mpp[a[i]]);
    }
    if (mpp.find(a[n - 1]) == mpp.end() || mpp.find(a[n - 2]) == mpp.end()) {
        pn;
        return;
    }
    int np1 = mpp[a[n - 1]] + (int)os.size() - (int)os.order_of_key(mpp[a[n - 1]]);
    int np2 = mpp[a[n - 2]] + (int)os.size() - (int)os.order_of_key(mpp[a[n - 2]]);
    if (np1 != n - 1 || np2 != n - 2)
        pn;
    else
        py;
}
